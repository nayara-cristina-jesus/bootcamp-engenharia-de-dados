# Convertento tipos

# Inteito para float:

preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

# Float para inteiro:

preco = 10.30
print(preco)

preco = int(preco)
print(preco)

# Conversao por divisão:

preco = 10
print(preco)

print(preco / 2)

print(preco // 2)

# Numérico para string:

preco = 10.50
idade = 28

print(str(preco))
print(str(preco))

texto = f"idade {idade} preco {preco}"
print(texto)

